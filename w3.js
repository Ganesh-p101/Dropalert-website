function showTopics(Categorie) {
  const sidebar = document.getElementById("sidebar");
  sidebar.innerHTML = `<h3>${topics[Categorie].title}</h3>`;
  const ul = document.createElement("ul");

  topics[Categorie].list.forEach((topic, index) => {
    const li = document.createElement("li");
    li.textContent = topic.title;
    li.onclick = () => showContent(Categorie, index);
    ul.appendChild(li);
  });

  sidebar.appendChild(ul);
  showContent(Categorie, 0); // Show first topic by default
}

function showContent(Categorie, index) {
  const contentBox = document.getElementById("contentBox");
  const topic = topics[Categorie].list[index];
  contentBox.innerHTML = `
    <h3>${topic.title}</h3>
    <p>${topic.content}</p>
  `;
}